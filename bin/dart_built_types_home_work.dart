void main(List<String> arguments) {
  String str06 = 'Kyrgyzcha Flutter15';
  print(str06);

  num num05 = 10;
  print(num05);

  int int04 = 28;
  print(int04);

  double double03 = 8.0;
  print(double03);

  bool bool02 = true;
  print(bool02);

  print('$str06,$num05,$int04,$double03,$bool02');

  print('\n$str06,\n$num05,\n$int04,\n$double03,\n$bool02');

  // String example.
  String firstName = 'Alymbek';
  String lastName = 'Uraanbek';
  print('Menin atym $firstName,atamdyn aty $lastName');

  //Num example.
  num san1 = 49;
  num san2 = 36;
  print('$san1+$san2=${san1 + san2}');

  //Int example.
  int san5 = 90;
  int san6 = 80;
  print('$san5+$san6=${san5 + san6}');

  //Bool example.
  bool correct = true;
  bool unCorrect = false;
  print('1+1=2 $correct');
  print('1+3=2 $unCorrect');

  //Dynamic.
  dynamic dynamic01 = 'Aiganish';
  print(dynamic01);
}

